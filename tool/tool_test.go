func TestUnlockETHWallet(t *testing.T) {
	address := "0x6dB7Ee9774Be5a16685241fCeF5d6f968d9b0259"
	keysDir := "../keystores"
	err1 := UnlockETHWallet(keysDir, address, "123")
	if err1 != nil {
		fmt.Println("第一次解锁错误", err1.Error())
	} else {
		fmt.Println("第一次解锁成功")
	}

	err2 := UnlockETHWallet(keysDir, address, "12345678")
	if err2 != nil {
		fmt.Println("第二次解锁错误", err2.Error())
	} else {
		fmt.Println("第二次解锁成功")
	}

	txData := types.LegacyTx{
		Nonce:    123,
		GasPrice: big.NewInt(20),
		Gas:      1000,
		To:       &common.Address{},
		Value:    new(big.Int).SetInt64(10),
		Data:     []byte("交易"),
	}
	tx := types.NewTx(&txData)
	signTx, err := SignETHTransaction(address, tx)
	if err != nil {
		panic(err)
	}
	data, _ := json.Marshal(signTx)
	fmt.Println(string(data))
}

